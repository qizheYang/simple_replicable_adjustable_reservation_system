type TranslationKey = keyof typeof translations.zh;

interface I18nContext {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: TranslationKey) => string;
  machineDisplay: (id: number) => string;
}

const I18nCtx = createContext<I18nContext>(null!);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem('rsvp_lang') as Lang;
    return saved && translations[saved] ? saved : 'zh';
  });

  const changeLang = (l: Lang) => {
    setLang(l);
    localStorage.setItem('rsvp_lang', l);
  };

  const t = (key: TranslationKey): string => {
    return (translations[lang] as any)[key] ?? (translations.zh as any)[key] ?? key;
  };

  const machineDisplay = (id: number): string => {
    const key = `machine.${id}` as TranslationKey;
    return t(key);
  };

  return (
    <I18nCtx.Provider value={{ lang, setLang: changeLang, t, machineDisplay }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() {
  return useContext(I18nCtx);
}
